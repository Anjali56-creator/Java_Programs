package Basic;
public class literals {
public static void main(String[] args) {
    //literals
    int n=0b1101; //binary literal
    System.out.println(n);
    int num1=0x1A; //hexadecimal literal
    System.out.println(num1);
    int n1=10_00_000; //using underscore in numeric literal
    System.out.println(n1);
    char c='a';
    c++;
    System.out.println(c);
}
}
