package conditional;

public class if_else {
    public static void main(String[] args)
    {
        int x=10;
        if(x>0)
        {
            System.out.println("x is positive " + x);
        }
        else
        {
            System.out.println("x is not positive " + x);
        }
        int a=5;
        int b=10;
        if(a>b)
        {
            System.out.println("a is greater than b " + a);
        }
        else
        {
            System.out.println("a is not greater than b " + a);
        }
    }
}
